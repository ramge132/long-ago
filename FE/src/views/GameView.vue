      try {
        
        return await deleteGame({
          gameId: gameID.value,
          isForceStopped: false
        }).then((res) => {
          
          if (res.data && res.data.data) {
            
            // 각 필드의 타입 체크
            
            // null/undefined 체크
            
            ISBN.value = res.data.data.bookId;
